// ############### If-Else Condition ###############

// Checking eligibility to vote
let age = 17;

// If age is 18 or more, user can vote
if(age >= 18){
   console.log("Can vote");
} else {
   // Otherwise, not eligible to vote
   console.log("Can't vote");
}

