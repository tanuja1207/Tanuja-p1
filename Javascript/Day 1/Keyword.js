//## Var Keyword

// #1. declare variable with var keyword.
var firstName = "Tanuja";
firstName = "Sanika";
var firstName = "Sneha"; 
console.log(firstName); // Output: Sneha

//## let Keyword

// #2. declare variable with let keyword.
let first_Name = "Tanuja"; 
first_Name = "Sanika"; 
console.log(first_Name); // Output: Sanika


//## const Keyword
// #3. declare constants
const pi = 3.14;
console.log(pi); // Output: 3.14

