let str = "Tanuja";

// T a n u j a
// 0 1 2 3 4 5

console.log(str[0]); // T
console.log(str[1]); // a
console.log(str[2]); // n
console.log(str[3]); // u
console.log(str[4]); // j
console.log(str[5]); // a

// Length of the string
console.log(str.length); // 6

// Last character using str.length - 1
console.log(str[str.length - 1]); // a
