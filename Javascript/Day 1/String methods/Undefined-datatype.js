// ############## Undefined ##################

// In case of var
var firstName;
console.log(typeof firstName); // "undefined"

// Extra: Assign a value
firstName = "Tanuja";
console.log(typeof firstName, firstName); // "string", "Tanuja"

// In case of let
let first;
console.log(typeof first); // "undefined"

// In case of const
// Must initialize while declaring
const first_Name = "Sanika";
console.log(typeof first_Name); // "string"
