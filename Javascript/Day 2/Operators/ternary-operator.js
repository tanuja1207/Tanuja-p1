// ################### Ternary Operator / Conditional Operator ###################

// if-else approach
let age = 15;
let drink;

if (age >= 5) {
    drink = "coffee";
} else {
    drink = "milk";
}

console.log(`${drink}`); // Output: coffee


// Using ternary operator 
let age1 = 22;

let drinkit = age1 >= 20 ? "Coffee" : "Milk";

console.log(`${drinkit}`); // Output: Coffee
