// ###################### Dot vs Bracket Notation ######################

// ## Dot Notation

const student = {
  name: "Tanuja",
  age: 21
};

console.log(student.name);  // "Tanuja"
console.log(student.age);   // 21


// ## Bracket Notation

const person = {
  "first name": "Tanuja",
  "last name": "Kadam"
};

console.log(person["first name"]); // "Tanuja"
console.log(person["last name"]);  // "Kadam"
